def motif(fasta_data):
    sequences = []
    current_sequence = ""

    for line in fasta_data.splitlines():
        if line.startswith(">"):
            if current_sequence:
                sequences.append(current_sequence)
            current_sequence = ""
        else:
            current_sequence += line

    if current_sequence:
        sequences.append(current_sequence)

    return sequences

