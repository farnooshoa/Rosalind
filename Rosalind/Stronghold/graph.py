'''
A solution to a ROSALIND bioinformatics problem.

Problem Title: Overlap Graphs
Rosalind ID: GRPH
Rosalind #: 012
URL: http://rosalind.info/problems/grph/
'''
def generate_overlap_graph(dna_sequences, k):
    overlap_graph = []

    for i in range(len(dna_sequences)):
        for j in range(len(dna_sequences)):
            if i != j and dna_sequences[i].seq[-k:] == dna_sequences[j].seq[:k]:
                overlap_graph.append((i + 1, j + 1))

    return overlap_graph

def main(fasta_records):
    # Generate overlap graph for k=3
    overlap_graph = generate_overlap_graph(fasta_records, k=3)

    # Output the pairs of identifiers
    for edge in overlap_graph:
        print(f"{fasta_records[edge[0] - 1].id} {fasta_records[edge[1] - 1].id}")

