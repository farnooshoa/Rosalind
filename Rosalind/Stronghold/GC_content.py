def highest_gc_content(fasta_data):
    sequences = {}  
    current_id = ""  
    gc_contents = {}  

    # Parse the input FASTA data
    for line in fasta_data.splitlines():
        if line.startswith(">"):
            # If a line starts with '>', it is an ID line
            current_id = line[1:]
            sequences[current_id] = ""
        else:
            # Otherwise, it is a sequence line, append it to the current sequence
            sequences[current_id] += line

    # Calculate GC-content for each sequence
    for seq_id, sequence in sequences.items():
        gc_content = (sequence.count("C") + sequence.count("G")) / len(sequence) * 100
        gc_contents[seq_id] = gc_content

    # Find the ID with the highest GC-content
    max_gc_id = max(gc_contents, key=gc_contents.get)
    max_gc_content = gc_contents[max_gc_id]

    return max_gc_id, max_gc_content

